number1 = int(input("Podaj pierwszą liczbę"))
number2 = int(input("Podaj drugą liczbę"))
operator = input("Podaj operator")


if operator == "+":
    print(number1 + number2)
elif operator == "-":
    print(number1 - number2)
elif operator == "*":
    print(number1 * number2)
elif operator == "/":
    print(number1 / number2)

